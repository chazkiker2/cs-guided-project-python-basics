"""
Challenge #2:

Write a function that takes an integer `minutes` and converts it to seconds.

Examples:
- convert(5) ➞ 300
- convert(3) ➞ 180
- convert(2) ➞ 120
"""


def convert(minutes):
    # Your code here
    return minutes * 60


print(f"convert(5) ➞ 300 -> {convert(5)}")
print(f"convert(3) ➞ 180 -> {convert(3)}")
print(f"convert(2) ➞ 120 -> {convert(2)}")
